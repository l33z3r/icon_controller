# === HARDWARE BASE ADDRESSES ===

# === DISPLAY ROWS (SysEx Addresses)
SCRIBBLE_1 = [0xF0, 0x00, 0x00, 0x66, 0x14, 0x12, 0x38]  # Row 1: Channel Names
SCRIBBLE_2 = [0xF0, 0x00, 0x00, 0x66, 0x14, 0x12, 0x00]  # Row 2: Channel Numbers
SCRIBBLE_3 = [0xF0, 0x00, 0x02, 0x4E, 0x15, 0x13, 0x00]  # Row 3: Sends Channels
SCRIBBLE_4 = [0xF0, 0x00, 0x02, 0x4E, 0x15, 0x13, 0x38]  # Row 4: Send Names

# === COLOR ROWS
COLOR_ROW = [0xF0, 0x00, 0x02, 0x4E, 0x16, 0x14] # LED RGB Colors (24 bytes)

# === STRIP BUTTONS
REC_CC_BASE     = 0x00  # Record buttons   (0x00–0x07)
SOLO_CC_BASE    = 0x08  # Solo buttons     (0x08–0x0F)
MUTE_CC_BASE    = 0x10  # Mute buttons     (0x10–0x17)
SELECT_CC_BASE  = 0x18  # Select buttons   (0x18–0x1F)

VPOT_CC = 0xB0  # Change Control Status
VPOT_OS = 0x30  # VPOT LEDs: 0x30-0x37 Hardware CH 0-7
PAN2_OS = 0x20  # Pan Press: 0x10-0x17 Hardware CH 0-7
PAN1_OS = 0x10  # Pan Turns: 0x20-0x27 Hardware CH 0-7
PAN_DIR = 0x40  # If (D2 & 0x40) != 0 → LEFT, If (D2 & 0x40) == 0 → RIGHT
MODE_PAN  = 0x1 # 0x10
MODE_DOT  = 0x2 # 0x20
MODE_FILL = 0x3 # 0x30
MODE_WRAP = 0x4 # 0x40


# === FADERS ===
PITCH_BEND_OS   = 0xE0  # Pitch Bend Status / Type
FADER_CC_BASE   = 0x30  # Fader offset: Hardware CH 0-7 (0x30 - 0x37)
M_FADER_CC_BASE = 0xE0  # Master Fader: Hardware CH 8

# === CLOCK ===
CLOCK_TOGGLE = 0x35  # Touch buttons    
CLOCK_DISPLAY = (0x49, 0x48, 0x47, 0x46, 0x45, 0x44, 0x43, 0x42, 0x41, 0x40) # 10-digit clock CCs, left → right

# === TRANSPORT BUTTONS
PLAY    = 0x5E
STOP    = 0x5D
ARM     = 0x5F
LOOP    = 0x56
REWIND  = 0x5B
FORWARD = 0x5C

# === METERS
METER_AFTERTOUCH_BASE = 0xD0
METER_MODE_VERTICAL   = 2
METER_TRACK           = [0xF0, 0x00, 0x02, 0x4E, 0x17, 0x15]
METER_MASTER          = [0xF0, 0x00, 0x02, 0x4E, 0x18, 0x16]

